import Head from 'next/head';
import Image from 'next/image';
import styles from '../styles/Home.module.css';
import { fetchGames } from '../lib/api-calls';
import Link from 'next/link';
import { FontAwesomeIcon } from '@fortawesome/react-fontawesome'
import { faHome, faMagnifyingGlass } from '@fortawesome/free-solid-svg-icons';

interface Game {
  _id: string;
  title: string;
  price: number;
  posterImg: string;
}

export async function getServerSideProps() {
  const games: Array<Game> = await fetchGames();
  return {
    props: {
      games,
    },
  };
}

interface Props {
  games: Game[];
}

export default function Home({ games }: Props) {
  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name='description' content='Generated by create next app' />
        <meta name='viewport' content='width=device-width, initial-scale=1' />
        <link rel='icon' href='/favicon.ico' />
      </Head>

      <nav className='navbar bg-lgray'>
        <div className='home-link'>
          <Link href='/'><FontAwesomeIcon icon={faHome} /></Link>
        </div>
        <form className='search-form'>
          <input type='search' name='search' id='' placeholder='Search' />
          <button type='submit'><FontAwesomeIcon icon={faMagnifyingGlass} /></button>
        </form>
        <ul>
          <li>
            <Link href='/register'>Register</Link>
          </li>
          <li>
            <Link href='/login'>Login</Link>
          </li>
        </ul>
      </nav>

      <div className='cart-container'></div>

      <section className='container'>
        <div className='games-list'>
          {games &&
            games.map((item) => {
              return (
                <div className='game-item' key={item._id}>
                  <Link href={`/games/${item._id}`}>
                    <Image
                      src={item.posterImg}
                      alt={item.title}
                      width={220}
                      height={280}
                    />
                    <h3>{item.title}</h3>
                    <span className='list-price'>{item.price}</span>
                  </Link>
                </div>
              );
            })}
        </div>
      </section>
    </>
  );
}
